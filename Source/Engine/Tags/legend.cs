namespace PowerUI{
	
	/// <summary>
	/// Represents a standard legend element.
	/// </summary>
	
	[Dom.TagName("legend")]
	public class HtmlLegendElement:HtmlElement{
		
		/// <summary>The align attribute.</summary>
		public string align{
			get{
				return getAttribute("align");
			}
			set{
				setAttribute("align", value);
			}
		}
		
	}
	
}